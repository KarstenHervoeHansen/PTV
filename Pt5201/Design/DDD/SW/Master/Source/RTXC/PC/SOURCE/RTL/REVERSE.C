#include "runtm.h"
#include "utilprot.h"

void reverse(char *s)  /* reverse string s in place */
{
    char c;
    int i, j;

    for (i = 0, j = strlen((const char *)s) - 1; i < j; i++, j--)
    {
        c = s[i];
        s[i] = s[j];
        s[j] = c;
    }
}

